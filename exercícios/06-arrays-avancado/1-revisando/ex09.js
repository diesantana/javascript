// 9.	Divida uma string em um array usando split():

const string = 'hello';
const arr = string.split('');
console.log(arr);
// [ 'h', 'e', 'l', 'l', 'o' ]

const frase = 'A rápida raposa marrom';
const palavras = frase.toLowerCase().split(' ');
console.log('palavras = ', palavras);
// palavras =  [ 'a', 'rápida', 'raposa', 'marrom' ]
